# Creating a tuple
a = (1, 2, 3, 2, 4, 2)

# count() -> Counts occurrences of a value
print("Count of 2:", a.count(2))

# index() -> Returns first index of a value
print("Index of 3:", a.index(3))

# Length of tuple
print("Length:", len(a))

# Accessing elements
print(a[0])

# Checking membership
print("2 in tuple:", 2 in a)